package Controller.AddFilm;

/**
 * Interface définissant les comportements de base d'un contrôleur pour l'ajout de films.
 * Cette interface garantit que tout contrôleur chargé de gérer l'ajout de films implémentera les méthodes nécessaires pour démarrer l'interaction avec l'interface utilisateur.
 */
public interface ControllerAddFilmInterface {

    /**
     * Méthode pour démarrer et afficher l'interface utilisateur associée au contrôleur.
     * Cette méthode doit être implémentée pour initialiser et rendre visible l'interface de gestion des films.
     */
    void start();
}
